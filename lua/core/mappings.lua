-- n, v, i, t = mode names

local function termcodes(str)
  return vim.api.nvim_replace_termcodes(str, true, true, true)
end

local M = {}

M.general = {
  i = {
    -- go to  beginning and end
    ["<C-b>"] = { "<ESC>^i", "beginning of line" },
    ["<C-e>"] = { "<End>", "end of line" },

    -- navigate within insert mode
    ["<C-h>"] = { "<Left>", "move left" },
    ["<C-l>"] = { "<Right>", "move right" },
    ["<C-j>"] = { "<Down>", "move down" },
    ["<C-k>"] = { "<Up>", "move up" },

    -- undo break points
    [","] = { ",<c-g>u", "add undo point" },
    ["."] = { ".<c-g>u", "add undo point" },
    ["?"] = { "?<c-g>u", "add undo point" },
    ["!"] = { "!<c-g>u", "add undo point" },
    [":"] = { ":<c-g>u", "add undo point" },
    ['"'] = { '"<c-g>u', "add undo point" },
    ["("] = { "(<c-g>u", "add undo point" },
    [")"] = { ")<c-g>u", "add undo point" },

    -- correct last wrong spelling
    ["<C-s>"] = { "<c-g>u<Esc>[s1z=`]a<c-g>u", "correct last wrong spelling" },
  },

  n = {
    ["<ESC>"] = { "<cmd> noh <CR>", "no highlight" },

    -- switch between windows
    ["<C-h>"] = { "<C-w>h", "window left" },
    ["<C-l>"] = { "<C-w>l", "window right" },
    ["<C-j>"] = { "<C-w>j", "window down" },
    ["<C-k>"] = { "<C-w>k", "window up" },

  -- Make adjusting split sizes a bit more friendly (but still unintuitive)
  ["<C-Left>"] = { "<cmd> vertical resize +3<CR>" },
  ["<C-Right>"] = { "<cmd> vertical resize -3<CR>" },
  ["<C-Up>"] = { "<cmd> resize +3<CR>" },
  ["<C-Down>"] = { "<cmd> resize -3<CR>" },

    -- Copy all
    ["<C-c>"] = { "<cmd> %y+ <CR>", "copy whole file" },

    -- Allow moving the cursor through wrapped lines with j, k, <Up> and <Down>
    -- http://www.reddit.com/r/vim/comments/2k4cbr/problem_with_gj_and_gk/
    -- empty mode is same as using <cmd> :map
    -- also don't use g[j|k] when in operator pending mode, so it doesn't alter d, y or c behaviour
    ["j"] = { 'v:count || mode(1)[0:1] == "no" ? "j" : "gj"', opts = { expr = true } },
    ["k"] = { 'v:count || mode(1)[0:1] == "no" ? "k" : "gk"', opts = { expr = true } },
    ["<Up>"] = { 'v:count || mode(1)[0:1] == "no" ? "k" : "gk"', opts = { expr = true } },
    ["<Down>"] = { 'v:count || mode(1)[0:1] == "no" ? "j" : "gj"', opts = { expr = true } },

    -- new buffer
    ["<leader>b"] = { "<cmd> enew <CR>", "new buffer" },

    -- Do not yank when changing
    ["c"] = { '"_c', "change without copying" },

    -- toggle theme
    ["<leader>tt"] = {
      function()
        require("base46").toggle_theme()
      end,
      "toggle theme",
    },

    -- update plugins
    ["<leader>P"] = { "<cmd> PackerSync<CR>", "update plugins" },

    -- replace all
    ["S"] = { ":%s//g<Left><Left>", "replace all" },

    -- toggle spelling
    ["<leader>s"] = { "<cmd> setlocal spell! spelllang=en_gb<CR>", "toggle spellcheck" },

    -- Compile document, be it groff/LaTeX/markdown/etc (requires external script)
    ["<leader>c"] = { ':w! | !compiler "<c-r>%"<CR>', "compile document" },

    -- Open corresponding .pdf/.html (requires external script)
    ["<leader>o"] = { ":!opout <c-r>%<CR><CR>", "open compiled output" },

    -- Open LaTeX bibliography file in split
    ["<leader>B"] = { "<cmd> vsp<space>$BIB<CR>", "open bibliography" },
  },

  t = { ["<C-x>"] = { termcodes "<C-\\><C-N>", "escape terminal mode" } },

  v = {
    ["<Up>"] = { 'v:count || mode(1)[0:1] == "no" ? "k" : "gk"', opts = { expr = true } },
    ["<Down>"] = { 'v:count || mode(1)[0:1] == "no" ? "j" : "gj"', opts = { expr = true } },

    -- Keep selectin when indenting visual block
    ["<"] = { "<gv", "keep visual selevtion when indenting block" },
    [">"] = { ">gv", "keep visual selevtion when indenting block" },

  -- Perform dot commands over visual blocks
    ["."] = { "<cmd> normal .<CR>", "perform last command on visual block" },
  },

  x = {
    ["j"] = { 'v:count || mode(1)[0:1] == "no" ? "j" : "gj"', opts = { expr = true } },
    ["k"] = { 'v:count || mode(1)[0:1] == "no" ? "k" : "gk"', opts = { expr = true } },
    -- Don't copy the replaced text after pasting in visual mode
    -- https://vim.fandom.com/wiki/Replace_a_word_with_yanked_text#Alternative_mapping_for_paste
    ["p"] = { 'p:let @+=@0<CR>:let @"=@0<CR>', opts = { silent = true } },
  },

    -- Save file as sudo on files that require root permission
  c = { ["w!!"] = { "execute \'silent! write !sudo tee % >/dev/null\' <bar> edit!", "save as su" } },
}

